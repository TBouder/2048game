#include "../ft_game.h"

void	ft_print_name(int max_x, int max_y)
{
	int y;
	int x;

	y = max_y / max_y;
	x = (max_x / 2) - 23;
	mvprintw(y, x, " #########  ########    ###     ###   ####### ");
	mvprintw(++y, x, " #########  ########    ## ## ## ##   ##      ");
	mvprintw(++y, x, " ##         ##    ##    ##  ###  ##   ##      ");
	mvprintw(++y, x, " ##   ####  ########    ##       ##   ####### ");
	mvprintw(++y, x, " ##     ##  ##    ##    ##       ##   ##      ");
	mvprintw(++y, x, " #########  ##    ##    ##       ##   ##      ");
	mvprintw(++y, x, " #########  ##    ##    ##       ##   ####### ");
	y += 2;
	mvprintw(++y, x, "       #####    ###    #        ##### ");
	mvprintw(++y, x, "      #    #  #     #  #    #  #     # ");
	mvprintw(++y, x, "           #  #     #  #    #  #     # ");
	mvprintw(++y, x, "      #####   #     #  #    #   #####  ");
	mvprintw(++y, x, "     #        #     #  ####### #     # ");
	mvprintw(++y, x, "     #         #   #        #  #     # ");
	mvprintw(++y, x, "     #######    ###         #   #####  ");
}
